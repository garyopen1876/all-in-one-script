function install-dev-tools(){
	# Install Chocolatey
	Write-Host "Install Chocolatey"
	[System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072; 
	iex ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))


	# Chocolatey gui
	Write-Host "Install Chocolatey gui"
	choco install -y chocolateygui

	# node.js
	Write-Host "Install node.js"
	choco install -y nodejs

	# python 3
	Write-Host "Install python 3"
	choco install -y python3

	# 7z
	Write-Host "Install 7z"
	choco install -y 7zip.install

	# git
	Write-Host "Install git"
	choco install -y git 
	
	# virtualbox
	Write-Host "Install virtualbox"
	choco install -y virtualbox
	
	# ngrok
	Write-Host "Install ngrok"
	choco install -y ngrok
	
	# docker
	Write-Host "Install docker"
	choco install -y docker-desktop
	
	# vscode
	Write-Host "Install vscode"
	choco install -y vscode
	
	# nodepad++
	Write-Host "Install nodepad++"
	choco install -y notepadplusplus
	
	# mysql
	Write-Host "Install mysql"
	choco install -y mysql
}


